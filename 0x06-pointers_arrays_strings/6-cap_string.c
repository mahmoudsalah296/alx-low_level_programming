#include <stdio.h>
#include "main.h"

char *cap_string(char *s)
{
	int i, j;

	if (s[0] >= 'a' && s[0] <= 'z')
	{
		s[0] -= ('a' - 'A');
	}
	for (i = 1; s[i] != '\0'; i++)
	{
		if (s[i] == ' ' || s[i] == '\t' || s[i] == '\n' || s[i] == ',' ||
				s[i] == ';' || s[i] == '.' || s[i] == '!' || s[i] == '?' ||
				s[i] == '\"' || s[i] == '(' || s[i] == ')' || s[i] == '{' ||
				s[i] == '}' || s[i] == '-')
		{
			j = i + 1;
			while (s[j] != '\0' && (s[j] == ' ' || s[j] == '\t' || s[j] == '\n' ||
						s[j] == ',' || s[j] == ';' || s[j] == '.' || s[j] == '!' ||
						s[j] == '?' || s[j] == '\"' || s[j] == '(' || s[j] == ')' ||
						s[j] == '{' || s[j] == '}' || s[j] == '-')) {
				j++;
			}
			if (s[j] >= 'a' && s[j] <= 'z')
				s[j] -= ('a' - 'A');
		}
	}
	return (s);
}
